-- Create DELIVERY_ADDRESS view
CREATE VIEW "DELIVERY_ADDRESS" AS
SELECT d.del_num, d.order_num, d.vehicle_num, (c.first_name || ' ' || c.last_name) AS CUST_NAME, c.phone_num, (c.UNIT_NUM || ', ' || ad.STREET || ', ' || ad.SUBURB || ', ' || ad.postcode) AS CUST_ADDRESS
FROM DELIVERY d, PPPORDER po, CUSTOMER c, ADDRESS ad
WHERE d.order_num = po.order_num
AND po.cust_num = c.cust_num
AND c.street = ad.street;

-- Create SERVICE_SCHEDULE view
CREATE VIEW "SERVICE_SCHEDULE" AS
SELECT v.VEHICLE_NUM, LAST_CHECKED_DATE, MILEAGE, COST_OF_MAINT, ADD_MONTHS(LAST_CHECKED_DATE, 12) AS NEXT_SERVICE_DATE
FROM VEHICLE v, VEHICLE_MAINT vm
WHERE v.vehicle_num = vm.vehicle_num
AND LAST_CHECKED_DATE BETWEEN ADD_MONTHS(SYSDATE, -12) AND SYSDATE
ORDER BY NEXT_SERVICE_DATE
